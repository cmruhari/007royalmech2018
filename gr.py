
# importing the required module 
import matplotlib.pyplot as plt
  # x axis values 
x = [1,1.1,1.3] 
# corresponding y axis values 
y = [0.002,0.0044,0.008] 
  # plotting the points  
plt.plot(x, y) 
  # naming the x axis 
plt.xlabel('x - axis') 
# naming the y axis 
plt.ylabel('y - axis') 
  # giving a title to my graph 
plt.title('My first graph!') 
  # function to show the plot 
plt.show() 
